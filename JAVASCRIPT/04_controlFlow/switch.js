const day = "wednesday"
switch (day) {
    case "mon":
        console.log("Monday");
        break;
    case "tues":
        console.log("Tuesday");
        break;
    case "wednesday":
        console.log("Wednesday");
        break;
    case "thurs":
        console.log("Thursday");
        break;
    case "fri":
        console.log("Friday");
        break;
    case "sat":
        console.log("Saturday");
        break;
    case "sun":
        console.log("Sunday");
        break;
    default:
        console.log("default case");
        break;
}
